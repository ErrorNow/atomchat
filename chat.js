// Push helper
var ac_dlay = 5000;
var ac_http = null;
var ac_init = 0;
var ac_link, ac_rand, ac_res, ac_div = "";

// Configure AJAX object
function ac_obj() {

    if (window.ActiveXObject) {
        return new ActiveXObject("Microsoft.XMLHTTP");
    } else if (window.XMLHttpRequest) {
        return new XMLHttpRequest();
    } else {
        alert("Your browser does not support AJAX!");
        return null;
    }
}

// Container state
function ac_set() {

    if (ac_http.readyState == 4) {
        ac_res           = ac_http.responseText;
        ac_div           = document.getElementById("ac_push");
        ac_div.innerHTML = ac_res;
        ac_div.scrollTop = ac_div.scrollHeight;
    }
}

// Configure timer
function ac_time() {
    ac_http = ac_obj();
    ac_rand = Math.floor(Math.random()*10000);

    if (ac_http != null) {
        ac_link = "?"+ac_rand;
        ac_http.open("GET", ac_link, true);
        ac_http.onreadystatechange = ac_set;
        ac_http.send(null);
    }
}

// Update screen
function ac_push() {
    ac_time();
    ac_init = setTimeout('ac_push()', ac_dlay);
}

